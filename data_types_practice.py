# Integer Example
age=37
score=95
print("Age:",age)
print("Score:",score)

# Float Example
weight="65.5kg"
height="5.9ft"  
print("Weight:",weight)
print("Height:",height)

# String Example   
name="Dawood"
address="Islamabad"
contact="03345085603"   
message="Hello, my name is Dawood. I live in Islamabad and my contact number is 03345085603."
print("Name:",name)
print("Address:",address)
print("Contact:",contact)
print("Message:",message)

# Boolean Example
is_student=True 
is_employed=False
print("Is Student:",is_student)     
print("Is Employed:",is_employed)
